// Paddle variables
const paddleHeight = 10;
const paddleWidth = 75;
let paddleX = (canvas.width - paddleWidth) / 2;

// Ball variables
let ballRadius = 10;
let x = canvas.width / 2;
let y = canvas.height - 30;
let dx = 2;
let dy = -2;

// Event listeners for paddle movement
let rightPressed = false;
let leftPressed = false;
document.addEventListener("keydown", keyDownHandler, false);
document.addEventListener("keyup", keyUpHandler, false);

function keyDownHandler(e) {
    if (e.key === "Right" || e.key === "ArrowRight") {
        rightPressed = true;
    } else if (e.key === "Left" || e.key === "ArrowLeft") {
        leftPressed = true;
    }
}

function keyUpHandler(e) {
    if (e.key === "Right" || e.key === "ArrowRight") {
        rightPressed = false;
    } else if (e.key === "Left" || e.key === "ArrowLeft") {
        leftPressed = false;
    }
}

// Draw the lung pattern
function drawPattern() {
    for (let row = 0; row < rows; row++) {
        for (let col = 0; col < cols; col++) {
            if (pattern[row][col] === 1) {
                ctx.fillStyle = "#FFFFFF"; // White color for the bricks
                ctx.fillRect(col * cellSize, row * cellSize, cellSize, cellSize);
                ctx.strokeStyle = "#000000"; // Black color for the border
                ctx.strokeRect(col * cellSize, row * cellSize, cellSize, cellSize);
            }
        }
    }
}

// Draw the paddle
function drawPaddle() {
    // Draw the tip of the cigarette (orange)
    ctx.fillStyle = "#FFA500";
    ctx.fillRect(paddleX, canvas.height - paddleHeight, cellSize, paddleHeight);
    
    // Draw the main part of the cigarette (white)
    ctx.fillStyle = "#FFFFFF";
    ctx.fillRect(paddleX + cellSize, canvas.height - paddleHeight, paddleWidth - cellSize, paddleHeight);
}

// Draw the cube ball
function drawCubeBall() {
    // Draw the main cube
    ctx.fillStyle = "#FFFFFF";
    ctx.fillRect(x - cellSize / 2, y - cellSize / 2, cellSize, cellSize);
    
}

// Collision detection
function collisionDetection() {
    for (let row = 0; row < rows; row++) {
        for (let col = 0; col < cols; col++) {
            if (pattern[row][col] === 1) {
                const brickX = col * cellSize;
                const brickY = row * cellSize;
                if (
                    x > brickX &&
                    x < brickX + cellSize &&
                    y > brickY &&
                    y < brickY + cellSize
                ) {
                    dy = -dy;
                    pattern[row][col] = 0; // Remove the brick
                }
            }
        }
    }
}

// Draw function to render the game
function draw() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    drawPattern();
    drawCubeBall();
    drawPaddle();
    collisionDetection();

    // Ball movement
    x += dx;
    y += dy;

    // Ball collision with walls
    if (x + dx > canvas.width - ballRadius || x + dx < ballRadius) {
        dx = -dx;
    }
    if (y + dy < ballRadius) {
        dy = -dy;
    } else if (y + dy > canvas.height - ballRadius) {
        if (x > paddleX && x < paddleX + paddleWidth) {
            dy = -dy;
        } else {
            document.location.reload();
        }
    }

    // Paddle movement
    if (rightPressed && paddleX < canvas.width - paddleWidth) {
        paddleX += 7;
    } else if (leftPressed && paddleX > 0) {
        paddleX -= 7;
    }

    requestAnimationFrame(draw);
}

draw();

